# 산술 연삱
a = 2
b = 4
ret1 = a+b
ret2 = a-b
ret3 = a*b
ret4 = a/b
ret5 = a//b
ret6 = a**b
ret7 = a+a*b/a
ret8 = a*b**a

print('a+b:', ret1)
print('a-b:', ret2)
print('a*b:', ret3)
print('a/b:', ret4)
print('a//b:', ret5)
print('a**b:', ret6)
print('a+a*b/a:', ret7)
print('a*b**a:', ret8)  # 2 * 4의 2제곱

