# 내장 함수

# 절대값 : abs()
print(abs(-10))

# 반올림 : round()
print(round(3.141592, 2))
print(round(3.6))

# 최대값 : max()
print(max(10, 30, 50))
print(max([100, 300, 200]))
print(max('Hello World'))

# range(초기값, 최종값, 중간값)
print(range(10))                   # 0 ~ 9
print(range(3, 10))                # 3 ~ 9
print(range(3, 10, 2))             # 3,5,7,9

